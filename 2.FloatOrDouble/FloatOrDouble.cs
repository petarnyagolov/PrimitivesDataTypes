using System;

class FloatOrDouble
{
    static void Main()
    {
        // 34.567839023, 12.345, 8923.1234857, 3456.091?
        float firstF = 12.345f;
        float secondF = 3456.091f;
        double firstDouble = 34.567839023f;
        double secondDouble = 8923.1234857f;

        Console.WriteLine(firstF);
        Console.WriteLine(secondF);
        Console.WriteLine(firstDouble);
        Console.WriteLine(secondDouble);
    }
}

